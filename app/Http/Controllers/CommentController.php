     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request, Food $food)
    {
        $page_size = $request->page_size ?? 3;
        $comments = Comment::where('foods_id', $food->id)->orderBy('created_at', 'desc')->paginate($page_size);

        return $comments;
    }

